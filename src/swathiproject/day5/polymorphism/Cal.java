package swathiproject.day5.polymorphism;

public class Cal {
	

		public int addNumbers(int i, int j) {
			return i + j;
		}

		public int addNumbers(int i, int j, int k) {
			return i + j + k;
		}

		public int addNumbers(int i, int j, int k, int l) {
			return i + j + k + l;
		}

	}

